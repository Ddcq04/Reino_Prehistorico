            <h2>Duración</h2>
            <p>
                El Paleógeno abarcó desde hace <strong class="texto-paleogeno">66 hasta 23 millones de años</strong>,
                siendo el primer periodo de la Era Cenozoica. Comenzó inmediatamente después
                de la extinción masiva del Cretácico-Paleógeno.
            </p>
        </section>

        <section id="vidrio" class="divisiones divisiones-paleogeno">
            <h2>Divisiones del Paleógeno</h2>
            <p>El Paleógeno se divide en tres épocas principales:</p>

            <ul>
                <li>Paleoceno (66 - 56 Ma)</li>
                <li>Eoceno (56 - 33.9 Ma)</li>
                <li>Oligoceno (33.9 - 23 Ma)</li>

            </ul>
        </section>

        <section  id="vidrio" class="recuperacion-postextincion efecto-recuperacion">
            <h2>Recuperación Post-Extinción</h2>
            <p>
                Tras la extinción de los dinosaurios no avianos, el planeta experimentó
                una <strong>recuperación gradual</strong> de los ecosistemas. Los nichos
                ecológicos dejados vacíos por los dinosaurios fueron ocupados por otros
                grupos de animales.
            </p>
        </section>

        <section id="vidrio" class="clima efecto-tierra">
            <h2>Clima</h2>
            <p>
                El clima del Paleógeno fue generalmente <strong>cálido y húmedo</strong>,
                especialmente durante el Eoceno (conocido como el "Óptimo Climático del Eoceno").
                Hacia el final del Oligoceno comenzó un <strong>enfriamiento global</strong>
                que preparó el escenario para las glaciaciones posteriores.
            </p>
        </section>

        <section id="vidrio" class="mamiferos mamiferos-paleogeno">
            <h2>La Radiación de los Mamíferos</h2>
            <p>
                Los mamíferos, que habían existido como pequeños animales nocturnos durante
                la era de los dinosaurios, experimentaron una <strong>explosión evolutiva</strong>:
            </p>
            <ul>
                <li><strong>Primates</strong> - Aparecieron los primeros simios</li>
                <li><strong>Caballos primitivos</strong> (Hyracotherium)</li>
                <li><strong>Primeros cetáceos</strong> - Mamíferos que regresaron al mar</li>
                <li><strong>Grandes aves depredadoras</strong> (como Gastornis)</li>
                <li>Primeros roedores, murciélagos y carnívoros</li>
            </ul>
        </section>

        <section id="vidrio" class="flora">
            <h2>Flora</h2>
            <p>
                Las plantas con flores (angiospermas) continuaron diversificándose y
                dominando los paisajes:
            </p>
            <ul>
                <li>Bosques tropicales extensos</li>
                <li>Primeras hierbas y pastizales</li>
                <li>Expansión de árboles de hoja caduca</li>
                <li>Palmeras en latitudes altas (debido al clima cálido)</li>
            </ul>
        </section>

        <section id="vidrio" class="fauna">
            <h2>Fauna Característica</h2>
            <p>Además de los mamíferos, destacaron:</p>
            <ul>
                <li><strong>Aves gigantes</strong> como Gastornis (depredadores terrestres)</li>
                <li>Reptiles grandes como las primeras serpientes constrictoras</li>
                <li>Tiburones y rayas modernas en los océanos</li>
                <li>Insectos diversificándose junto a las plantas con flores</li>
            </ul>
        </section>

        <section id="vidrio" class="eventos-importantes">
            <h2>Eventos Importantes</h2>
            <ul>
                <li><strong>Máximo Térmico del Paleoceno-Eoceno</strong> - Calentamiento global abrupto</li>
                <li>Formación de la corriente circumpolar antártica</li>
                <li>Colisión del subcontiente indio con Asia (formación del Himalaya)</li>
                <li>Aislamiento de Australia y Sudamérica</li>
            </ul>
        </section>

        <section id="vidrio" class="importancia">
            <h2>Importancia del Paleógeno</h2>
            <p>
                El Paleógeno estableció las bases para el mundo moderno. Los mamíferos
                se diversificaron en todas las formas que conocemos hoy, y los ecosistemas
                terrestres y marinos adquirieron una estructura similar a la actual.
                Este periodo demostró la <strong>resiliencia de la vida</strong> después
                de una extinción masiva.
            </p>
        </section>

