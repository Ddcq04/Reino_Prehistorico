    <section class="geografia" id="vidrio" >
        <h2>Geografía y continentes</h2>
        <p>
            Durante el Jurásico, el supercontinente <strong>Pangea comenzó a separarse</strong> en dos grandes masas:
            <strong>Laurasia</strong> al norte y <strong>Gondwana</strong> al sur. Esto creó nuevas costas y mares interiores.
        </p>
    </section>

    <section class="clima" id="vidrio">
        <h2>Clima</h2>
        <p>
            El clima fue <strong>cálido y húmedo</strong> en general, con lluvias más abundantes que en el Triásico.
            Esto permitió el crecimiento de exuberantes bosques y selvas por todo el planeta.
        </p>
    </section>

    <section class="flora" id="vidrio">
        <h2>Flora</h2>
        <p>La vegetación se volvió más diversa y abundante:</p>
        <ul>
            <li>Bosques de coníferas gigantes</li>
            <li>Helechos arborescentes</li>
            <li>Cícadas y ginkgos</li>
            <li>Primeras plantas con flores (al final del período)</li>
        </ul>
    </section>

    <section class="fauna" id="vidrio">
        <h2>Fauna</h2>
        <p>
            Los dinosaurios alcanzaron su máximo esplendor y tamaño. Aparecieron gigantes como:
        </p>
        <ul>
            <li><strong>Brachiosaurus</strong> y <strong>Diplodocus</strong> - saurópodos gigantes</li>
            <li><strong>Stegosaurus</strong> - dinosaurio acorazado</li>
            <li><strong>Allosaurus</strong> - gran depredador</li>
            <li>Primeras aves (<strong>Archaeopteryx</strong>)</li>
        </ul>
    </section>

    <section class="mar-jurasico" id="vidrio">
